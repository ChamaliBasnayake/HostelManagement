    public void recallData()
    {
          try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hosteldatabase", "root","");
            String query ="SELECT * FROM `early late pass details`  where Status = 'N'  limit 1 ";
            PreparedStatement pstmt = con.prepareStatement(query);
            ResultSet rs = pstmt.executeQuery();
            if(rs.next())
            {
                No.setText(rs.getString("EOLNO"));
                date.setText(rs.getString("Date"));
                rdate.setText(rs.getString("RequiredDate"));
                rtime.setText(rs.getString("RequiredTime"));
                type.setText(rs.getString("Type"));
                reason.setText(rs.getString("Reason"));
                rposition.setText(rs.getString("RequirestedPerson"));
                mobiposi.setText(rs.getString("RequestedPersonMobile"));
                
                String q = "select * from `requested students`  where EOLNO = '"+No.getText()+"'";
                PreparedStatement pm = con.prepareStatement(q);
                ResultSet rset = pm.executeQuery();
                DefaultTableModel dm = (DefaultTableModel) tbl.getModel();
                int i =1;
                while(rset.next())
                {
                    Object o[] = {i,rset.getString("RegNo")};
                    dm.addRow(o);
                    i++;
                }
                rset.close();
                pm.close();
            }  
            rs.close();
            pstmt.close();
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }
